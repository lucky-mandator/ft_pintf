#include "ft_printf.h"

size_t	ft_check_sp(char c)
{
	return (c == 'c' || c == 's' || c == 'p' || c == 'd' || c == 'i' ||
			c == 'u' || c == 'x' || c == 'X' || c == '%' || c == 'n');
}

size_t	ft_check_pf(char c)
{
	return (ft_check_sp(c) || c == '+' || c == '*' ||
			c == '-' || c == '.' || c == '#' || c == ' '
			|| (c >= '0' && c <= '9'));
}

size_t	ft_check_flags(char c)
{
	return (c == '+' || c == ' ' || c == '-' || c == '#' || c == '0');
}
